package com.collections.arraylist;

public class SamplePattern {
	/*
	 * 
	 *   @ output should be 
	 *    *
	 *    ***
	 *    *
	 *    
	 */
	public static void main(String a[]){
		
		
		String star="*";
		int n=1;
		
	for (int i = 1; i <=n; i++) {
		
		
			System.out.println(""+star);

	}
	n++;
	for (int i = 1; i <=n; i++) {
		
		System.out.println(""+star);
	}
	
	}
}
